static void RenderSceneCB()
{
    glClear(GL_COLOR_BUFFER_BIT);

    static float Scale = 0.0f;

    Scale += 0.01f;

    Matrix4f World;


    /*
    ¬ функции рендера мы подготавливаем матрицу 4x4 и заполн€ем в соответствии с объ€снением выше. 
    ћы устанавливаем v2 и v3 в 0, поэтому у объекта координаты Y и Z не будут измен€тьс€, и мы записываем в v1 значени€ синуса. 
    Ёто будет измен€ть координату X на значение, плавно переход€щее от -1 и до 1. “еперь мы загрузим матрицу в шейдер.
    */
    World.m[0][0] = 1.0f; World.m[0][1] = 0.0f; World.m[0][2] = 0.0f; World.m[0][3] = sinf(Scale);
    World.m[1][0] = 0.0f; World.m[1][1] = 1.0f; World.m[1][2] = 0.0f; World.m[1][3] = 0.0f;
    World.m[2][0] = 0.0f; World.m[2][1] = 0.0f; World.m[2][2] = 1.0f; World.m[2][3] = 0.0f;
    World.m[3][0] = 0.0f; World.m[3][1] = 0.0f; World.m[3][2] = 0.0f; World.m[3][3] = 1.0f;

     //загружает эту матрицу в шейдер. параметры:
     //1) представл€ет индекс, который синхронизирует эту матрицу cpp с матрицей в шейдере
     //2) количество матриц, которые мы планировали отправить
     //3) основной р€д (true) или основной столбец (false)
     //4) адрес массива
    glUniformMatrix4fv(gWorldLocation, 1, GL_TRUE, &World.m[0][0]);

    glEnableVertexAttribArray(0);
    glBindBuffer(GL_ARRAY_BUFFER, VBO);


    // параметры:
    //1)индекс атрибута - 0
    //2)количество элементов в этом атрибуте, 3 dim.vector
    //3)тип элементов
    //4) нормализовать данные до [-1,1]
    //5)шаг - количество байт до следующего по€влени€ этого атрибута в буфере
    //6) смещение от начала до местоположени€ первого экземпл€ра этого атрибута
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 0, 0);

    glDrawArrays(GL_TRIANGLES, 0, 3);

    glDisableVertexAttribArray(0);

    glutSwapBuffers();
}


